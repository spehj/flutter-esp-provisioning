/// MethodChannel argument names used in the Android and iOS plugins; must be modified with care!
class PluginArgumentNames {
  /// Argument name for the device name.
  static const deviceName = 'deviceName';

  /// Argument name for the provisioning service UUID.
  static const provisioningServiceUuid = 'provisioningServiceUuid';

  /// Argument name for device the proof of possession.
  static const proofOfPossession = 'proofOfPossession';

  /// Argument name for an Access Point's SSID.
  static const ssid = 'ssid';

  /// Argument name for an Access Point's password.
  static const password = 'password';

  /// Argument name for the endpoint path.
  static const endpointPath = 'endpointPath';

  /// Argument name for data in the form of a byte array.
  static const data = 'data';
}
